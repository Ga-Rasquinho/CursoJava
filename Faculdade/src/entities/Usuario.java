package entities;

public class Usuario {
	private int idUsuario;
	private String nome;
	private int tipoUsuario;
	
	public Usuario() {
		
	}

	public Usuario(int idUsuario, String nome, int tipoUsuario) {
		this.idUsuario = idUsuario;
		this.nome = nome;
		this.tipoUsuario = tipoUsuario;
	}

	
	
}
